# Criando variáveis com diferentes tipos de dados
nome_do_curso = "Guia de Python"  # str
ano_lancamento = 2024              # int
nota_avaliacao = 4.9               # float
esta_ativo = True                  # bool

# Exibindo os valores e os tipos de cada variável
print(nome_do_curso)
print(ano_lancamento)
print(nota_avaliacao)
print(esta_ativo)

# A função type() nos mostra o tipo de uma variável
print(type(nome_do_curso)) # <class 'str'>
print(type(ano_lancamento)) # <class 'int'>